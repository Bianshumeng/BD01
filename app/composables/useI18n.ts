function getDefaultLocale(): AppLocale {
  if (!import.meta.client) {
    return 'en'
  }

  return navigator.language.toLowerCase().startsWith('zh') ? 'zh-CN' : 'en'
}

function interpolate(template: string, params?: Record<string, string | number>) {
  if (!params) {
    return template
  }

  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = params[key]
    return value === undefined || value === null ? `{${key}}` : String(value)
  })
}

const localeRef = useLocalStorage<AppLocale>(LOCALE_STORAGE_KEY, getDefaultLocale())

export function useI18n() {
  const locale = localeRef
  const isChinese = computed(() => locale.value === 'zh-CN')

  const supportedLocales = SUPPORTED_LOCALES

  const setLocale = (nextLocale: AppLocale) => {
    locale.value = SUPPORTED_LOCALES.some(item => item.value === nextLocale) ? nextLocale : 'en'
  }

  const t = (key: string, params?: Record<string, string | number>) => {
    const translated = locale.value === 'zh-CN'
      ? (ZH_CN_MESSAGES[key] ?? key)
      : key

    return interpolate(translated, params)
  }

  return {
    locale,
    isChinese,
    supportedLocales,
    setLocale,
    t,
  }
}
